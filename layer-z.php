        <h1 class="cs-section-title">Logo Design</h1>
        <p class="very-big-paragraph-half">The logo is a mix of &#x27;creative&#x27; and &#x27;simple&#x27;. The idea is that systems in programming have 3 layers: x, y and z. The Z layer is the user-facing part of a system which is the whole concept of the software that Layer Z are developing. Hence the logo having 3 layers and the Z being on top (facing the users), simple but creative. The colours we have chosen for the logo support the aspect of being different and disruptive. </p>
