        <p class="very-big-paragraph">Our main aim was to truly understand what Layer Z wanted to be. To do this we have designed and carried out various creative workshops to extract the subconscious idea that wasn&#x27;t so easily translated into words. Through this immersive journey, we were able to truly SEE what Layer Z wanted to be. With all this information we gathered in the workshops, all we needed was data to push our decisions. This mix of workshops and research set the stage for creating Layer Z&#x27;s unique brand.</p>
